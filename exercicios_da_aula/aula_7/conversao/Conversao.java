package exercicios_da_aula.aula_7.conversao;

public class Conversao {
    public static double polegadasParaCentrimetros(double polegadas){
        return polegadas * 2.54; 
    }

    public static double pesParaCentrimetros(double pes){
        return pes * 3.48; 
    }

    public static double milhasParaQuilometros(double milhas){
        return milhas * 1.609; 
    }
}
